/*
    Quando so usado o IF sem o ELSE se chama CONDIÇÃO SIMPLES
    Quando é usado IF E ELSE se chama CONDIÇÃO COMPOSTA!
*/

var vel = 78.2;

console.log(`A velocidade do seu carro é ${vel}km/h`);

if(vel > 60){
    console.log('Você ultrapassou a velocidade permirida, VOCÊ ESTÁ MULTADO!');
}else{
    console.log(`Dirija sempre usando cinto de segurança!`);  
}